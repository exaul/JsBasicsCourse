var luckyNumber = 7;

switch (luckyNumber) {
  case 1:
    console.log("Is 1");
    break;
  case 8:
    console.log("Is 8");
    break;
  default:
    console.log("Default");
    break;
}

if (luckyNumber == 1) {
  console.log("Is 1");
} else if (luckyNumber == 8) {
  console.log("Is 8");
} else {
  console.log("Default");
}
